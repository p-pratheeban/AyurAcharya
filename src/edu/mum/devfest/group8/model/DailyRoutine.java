package edu.mum.devfest.group8.model;



public class DailyRoutine {
	private String name;
	private String date;
	private int wakeup;
	private int sleep;
	private int breakfasttime;
	private String brealfastmeal;
	private int lunchtime;
	private String lunchmeal;
	private int dinnertime;
	private String dinnermeal;
	private int meditation;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getWakeup() {
		return wakeup;
	}
	public void setWakeup(int wakeup) {
		this.wakeup = wakeup;
	}
	public int getSleep() {
		return sleep;
	}
	public void setSleep(int sleep) {
		this.sleep = sleep;
	}
	public int getBreakfasttime() {
		return breakfasttime;
	}
	public void setBreakfasttime(int breakfasttime) {
		this.breakfasttime = breakfasttime;
	}
	public String getBrealfastmeal() {
		return brealfastmeal;
	}
	public void setBrealfastmeal(String brealfastmeal) {
		this.brealfastmeal = brealfastmeal;
	}
	public int getLunchtime() {
		return lunchtime;
	}
	public void setLunchtime(int lunchtime) {
		this.lunchtime = lunchtime;
	}
	public String getLunchmeal() {
		return lunchmeal;
	}
	public void setLunchmeal(String lunchmeal) {
		this.lunchmeal = lunchmeal;
	}
	public int getDinnertime() {
		return dinnertime;
	}
	public void setDinnertime(int dinnertime) {
		this.dinnertime = dinnertime;
	}
	public String getDinnermeal() {
		return dinnermeal;
	}
	public void setDinnermeal(String dinnermeal) {
		this.dinnermeal = dinnermeal;
	}
	public int getMeditation() {
		return meditation;
	}
	public void setMeditation(int meditation) {
		this.meditation = meditation;
	}
	
	
	
}
